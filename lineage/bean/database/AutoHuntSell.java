package lineage.bean.database;

public class AutoHuntSell {
	private long objId;
	private String name;
	private String item;
	private int bless;
	private int en;
	
	public long getObjId() {
		return objId;
	}
	public void setObjId(long objId) {
		this.objId = objId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public int getBless() {
		return bless;
	}
	public void setBless(int bless) {
		this.bless = bless;
	}
	public int getEn() {
		return en;
	}
	public void setEn(int en) {
		this.en = en;
	}
}
