package lineage.bean.database;

public class MagicdollList {
	
	private String itemName;
	private String materialName;
	private int materialCount;
	private String dollName;
	private int dollGfx;
	private String dollBuffType;
	private int dollBuffEffect;
	private int dollContinuous;
	
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getMaterialName() {
		return materialName;
	}
	public void setMaterialName(String materialName) {
		this.materialName = materialName;
	}
	public int getMaterialCount() {
		return materialCount;
	}
	public void setMaterialCount(int materialCount) {
		this.materialCount = materialCount;
	}
	public String getDollName() {
		return dollName;
	}
	public void setDollName(String dollName) {
		this.dollName = dollName;
	}
	public int getDollGfx() {
		return dollGfx;
	}
	public void setDollGfx(int dollGfx) {
		this.dollGfx = dollGfx;
	}
	public String getDollBuffType() {
		return dollBuffType;
	}
	public void setDollBuffType(String dollBuffType) {
		this.dollBuffType = dollBuffType;
	}
	public int getDollBuffEffect() {
		return dollBuffEffect;
	}
	public void setDollBuffEffect(int dollBuffEffect) {
		this.dollBuffEffect = dollBuffEffect;
	}
	public int getDollContinuous() {
		return dollContinuous;
	}
	public void setDollContinuous(int dollContinuous) {
		this.dollContinuous = dollContinuous;
	}
	
}
